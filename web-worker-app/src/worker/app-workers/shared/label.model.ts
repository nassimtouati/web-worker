
export class Label {
    xo: number;
    yo: number;
    x: number;
    y: number;
    width: number;
    height: number;
    name: string;

    constructor(x: number, y: number, width: number, height: number, name: string) {
        this.x = x;
        this.y = y;
        this.xo = x;
        this.yo = y;
        this.width = width;
        this.height = height;
        this.name = name;
    }

// class
}
